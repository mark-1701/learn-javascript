// Métodos y funciones matemáticas en JavaScript utilizando el objeto Math

// Valor absoluto de un número
Math.abs(-5); // Devuelve 5

// Redondear hacia arriba
Math.ceil(3.2); // Devuelve 4

// Redondear hacia abajo
Math.floor(3.8); // Devuelve 3

// Redondear al entero más cercano
Math.round(3.5); // Devuelve 4

// Encontrar el valor más grande
Math.max(2, 8, 5, 12); // Devuelve 12

// Encontrar el valor más pequeño
Math.min(2, 8, 5, 12); // Devuelve 2

// Elevar a una potencia
Math.pow(2, 3); // Devuelve 8

// Calcular la raíz cuadrada
Math.sqrt(16); // Devuelve 4

// Generar un número aleatorio entre 0 y 1
Math.random(); // Devuelve un número aleatorio entre 0 y 1

// Funciones trigonométricas (en radianes)
Math.sin(Math.PI / 2); // Devuelve 1 (seno de 90 grados)

// Valor de Pi (π)
Math.PI; // Devuelve aproximadamente 3.141592653589793

// Número de Euler (e)
Math.E; // Devuelve aproximadamente 2.718281828459045
